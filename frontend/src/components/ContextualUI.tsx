export const ContextualUI: React.FC<ContextualUIProps> = ({ intent, onClose, onOpenTool }) => {
  const { uiConfig } = intent;
  const { t } = useTranslation();
  const { pinTool, unpinTool, isPinned } = usePinnedTools();

  const toolId = uiConfig.toolId || uiConfig.type;
  const pinned = isPinned(toolId);

  // Convert kebab-case tool ID to camelCase for i18n key lookup
  const toolIdToKey = (id: string) => id.replace(/-([a-z])/g, (_, c: string) => c.toUpperCase());

  // Get translated tool title
  const getTranslatedTitle = () => {
    const key = toolIdToKey(toolId);
    const translated = t(`toolComponents.${key}.title`, { defaultValue: '' });
    return translated || uiConfig.title;
  };

  // Get translated tool description
  const getTranslatedDescription = () => {
    if (!uiConfig.description) return undefined;
    const key = toolIdToKey(toolId);
    const translated = t(`toolComponents.${key}.description`, { defaultValue: '' });
    return translated || uiConfig.description;
  };

  // Get the proper icon for this tool - lookup from tools registry first, then fallback to toolIcons map
  const toolData = getAllTools().find(t => t.id === toolId);
  const iconName = toolData?.icon || toolIcons[toolId] || 'calculator';
  const ToolIconComponent = getToolIcon(iconName);

  const handlePin = () => {
    if (pinned) {
      unpinTool(toolId);
    } else {
      pinTool({
        toolId,
        title: uiConfig.title,
        description: uiConfig.description || '',
        icon: typeof uiConfig.icon === 'string' ? uiConfig.icon : toolIcons[toolId] || 'tool',
        type: uiConfig.type,
      });
    }
  };

  const renderTool = () => {
    // First try to get tool by specific toolId
    if (uiConfig.toolId && toolComponents[uiConfig.toolId]) {
      const ToolComponent = toolComponents[uiConfig.toolId] as React.FC<ToolComponentProps>;
      // Pass uiConfig to component for prefill data support
      // Components that don't use uiConfig will simply ignore it
      return <ToolComponent uiConfig={uiConfig} />;
    }

    // Fall back to default tool for the type
    const DefaultTool = defaultToolByType[uiConfig.type] as React.FC<ToolComponentProps> | null;
    if (DefaultTool) {
      // Also pass uiConfig to default tools
      return <DefaultTool uiConfig={uiConfig} />;
    }

    // Show placeholder for unimplemented tools
    return (
      <div className="text-gray-700 text-center py-12">
        <div className="w-16 h-16 mx-auto mb-4 bg-[#0D9488]/10 rounded-full flex items-center justify-center">
          <ToolIconComponent className="w-8 h-8 text-[#0D9488]" />
        </div>
        <p className="text-xl font-semibold mb-2">{uiConfig.title}</p>
        <p className="text-gray-500">This tool is coming soon!</p>
        <p className="text-sm text-gray-400 mt-2">Type: {uiConfig.type}</p>
        {uiConfig.toolId && (
          <p className="text-sm text-gray-400">Tool ID: {uiConfig.toolId}</p>
        )}
      </div>
    );
  };

  return (
    <div className="fixed inset-0 z-50 flex items-center justify-center p-4 bg-black/30 backdrop-blur-sm animate-fade-in">
      <div className="w-[95vw] max-w-[95vw] h-[95vh] max-h-[95vh] flex flex-col bg-white rounded-2xl shadow-2xl overflow-hidden border border-gray-200 animate-fade-in">
        {/* Header */}
        <div className="flex-shrink-0 bg-gradient-to-r from-white to-[#0D9488]/5 border-b border-gray-200 px-6 py-4">
          <div className="flex items-center justify-between">
            <div className="flex items-center gap-3">
              <div className="p-2 bg-[#0D9488]/10 rounded-lg border border-[#0D9488]/20">
                <ToolIconComponent className="w-6 h-6 text-[#0D9488]" />
              </div>
              <div>
                <h2 className="text-2xl font-bold text-gray-900">{getTranslatedTitle()}</h2>
                {uiConfig.description && (
                  <p className="text-sm text-gray-500 mt-1">{getTranslatedDescription()}</p>
                )}
              </div>
            </div>

            {/* Action buttons */}
            <div className="flex items-center gap-2">
              {/* Pin button */}
              <button
                onClick={handlePin}
                className={`p-2 rounded-lg transition-all ${
                  pinned
                    ? 'bg-[#0D9488] text-white'
                    : 'hover:bg-gray-100 text-gray-500 hover:text-gray-700'
                }`}
                title={pinned ? t('toolsModal.unpinTool') : t('toolsModal.pinToSidebar')}
              >
                {pinned ? <PinOff className="w-5 h-5" /> : <Pin className="w-5 h-5" />}
              </button>

              {/* Close button */}
              <button
                onClick={onClose}
                className="p-2 hover:bg-gray-100 rounded-lg transition-colors group ml-2"
                aria-label="Close"
              >
                <X className="w-6 h-6 text-gray-500 group-hover:text-gray-700 transition-colors" />
              </button>
            </div>
          </div>
        </div>

        {/* Content */}
        <div className="flex-1 p-6 overflow-y-auto bg-gray-50">
          {renderTool()}
        </div>

        {/* Related Tools Section */}
        {toolId && onOpenTool && (
          <RelatedToolsSection
            currentToolId={toolId}
            onToolSelect={onOpenTool}
          />
        )}

        {/* Decorative gradient border */}
        <div className="flex-shrink-0 h-1 bg-gradient-to-r from-transparent via-[#0D9488] to-transparent opacity-30" />
      </div>
    </div>
  );
};

// Related Tools Section component
const RelatedToolsSection: React.FC<{
  currentToolId: string;
  onToolSelect: (config: UIConfig) => void;
}> = ({ currentToolId, onToolSelect }) => {
  const { t } = useTranslation();
  const toolIdToKey = (id: string) => id.replace(/-([a-z])/g, (_, c: string) => c.toUpperCase());
  const relatedTools = toolSuggestionService.getRelatedTools(currentToolId, 4);

  if (relatedTools.length === 0) {
    return null;
  }

  const handleToolClick = (tool: typeof relatedTools[0]) => {
    const config: UIConfig = {
      type: tool.tool.type as any,
      toolId: tool.tool.id,
      title: tool.tool.title,
      description: tool.tool.description,
    };
    onToolSelect(config);
  };

  return (
    <div className="flex-shrink-0 border-t border-gray-200 bg-gray-50 px-6 py-3">
      <div className="flex items-center gap-2 mb-2">
        <Sparkles className="w-4 h-4 text-[#0D9488]" />
        <span className="text-xs font-medium text-gray-600">{t('toolsModal.relatedTools')}</span>
      </div>
      <div className="flex flex-wrap gap-2">
        {relatedTools.map((suggestion) => {
          const ToolIcon = getToolIcon(suggestion.tool.icon);
          return (
            <button
              key={suggestion.tool.id}
              onClick={() => handleToolClick(suggestion)}
              className="flex items-center gap-2 px-3 py-1.5 rounded-lg text-sm bg-white hover:bg-gray-100 text-gray-700 border border-gray-200 transition-all hover:scale-105 hover:shadow-sm"
            >
              <ToolIcon className="w-4 h-4 text-[#0D9488]" />
              <span>{t(`toolComponents.${toolIdToKey(suggestion.tool.id)}.title`, { defaultValue: suggestion.tool.title })}</span>
              <ChevronRight className="w-3 h-3 text-gray-400" />
            </button>
          );
        })}
      </div>
    </div>
  );
};
